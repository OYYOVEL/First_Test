class Calculator:
   """простейший калькулятор"""

   def multiply(self, x, y):
       """функция умножения"""
       return x * y

   def division(self, x, y):
       """функция деления"""
       return x / y

   def subtraction(self, x, y):
       """функция вычитания"""
       return x - y

   def adding(self, x, y):
       """функция сложения"""
       return x + y